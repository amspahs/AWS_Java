package test.two;

public class Calculator {	//여기서 계산기를 만들고
	
	
	void add(int x, int y) {
		int result = x + y;
		System.out.println(x + " + " + y +" = " + result);
	}
	
	void sub(int x, int y) {
		int result = x - y;
		System.out.println(x + " - " + y +" = " + result);
	}	

	void mul(int x, int y) {
		int result = x * y;
		System.out.println(x + " * " + y +" = " + result);
	}

	void div(int x, int y) {
		int result = x / y;
		System.out.println(x + " / " + y +" = " + result);
	}	
	
		
		
		
//사칙연산
//출력형태: 입력한 숫자 + 입력한 숫자 = 계산된 결과값
	
	
	
	
	
	
}
